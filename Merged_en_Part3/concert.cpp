int main(){
    long long int n, m; 
    cin >> n >> m; 

    multiset<long long int> s; 


    for(int i = 0; i < n; i++){
        long long int d;
        cin >> d;
        s.insert(d); 
    }
    
    debug(s); 

    while(m--){
        long long int e;
        cin >> e;
        auto it = s.upper_bound(e); 
        if(it != s.begin()){
            cout << *(--it) << endl;
            s.erase(it); 
        }
        else{
            cout << -1 << endl; 
        }
    }

}